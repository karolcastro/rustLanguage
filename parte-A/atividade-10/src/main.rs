//Ler um valor numérico inteiro e apresente uma 
//mensagem informando se o valor fornecido é par ou ímpar


fn main() {

    let number = 13;

    if number % 2 == 0 {
        println!("O número que você digitou é par {}",number );
    }

   else {
        println!("O número que você digitou é ímpar {}",number );
    }
    
    
}
